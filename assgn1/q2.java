class q2 {
    public static void main(String[] args) {
        
        int a,b,temp;
        a=10;
        b=20;
        System.out.println("Before swap:");
        System.out.println("val1: " + a);
        System.out.println("val2: " + b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swap:");
        System.out.println("val1: " + a);
        System.out.println("val2: " + b);
    }
}